# ------- ------------ ------------- 3 assignment variable in python

# Multiple variables and swapping

x = 5
y = 10
# before swapping
print("Before swapping x =", x, "and y =", y)
# Swapping values
x, y = y, x
# after swapping
print("After swapping x =", x, "and y =", y)

# benefit of this method is that it allows us to swap values without using a temporary variable.
# This is a concise and efficient way to swap values in Python.



